cadena="Esto es un ejemplo de cadena de texto que usaremos para para este ejercicio"
print(cadena)
'''Esto sirve para identrificar el caracter en la posicion 40'''
print(cadena[40])
'''Esto sirve para identrificar el caracter desde la posicion 40 hasta la 45'''
print(cadena[40:45])
'''Esto sirve para imprimir desde el princio hasta la posicion 20 (podriamos poner [0:20])'''
print (cadena[:20])
'''Esto sirve para imprimir desde la posicion 20 hasta el final'''
print(cadena [20:])
'''Esto sirve para imprimir desde la posicion -2 hasta el final (seria como ir desde atras hacia el principio de la cadena)'''
print (cadena [-2:])